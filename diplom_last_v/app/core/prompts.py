def get_recovery_prompt(
    athlete: dict,
    fatigue: int,
    sleep: int,
    pain: str,
) -> str:
    """Промпт для оценки восстановления."""
    risk = "🔴 ВЫСОКИЙ" if fatigue >= 8 else "🟡 СРЕДНИЙ" if fatigue >= 5 else "🟢 НИЗКИЙ"
    sleep_label = "отличное" if sleep >= 8 else "хорошее" if sleep >= 6 else "плохое"

    return f"""Дай экспертные рекомендации по восстановлению спортсмена.

ПРОФИЛЬ:
• {athlete['name']}, {athlete.get('sport', '—')}, {athlete.get('level', '—')}
• Нагрузка: {athlete.get('sessions_per_week', '—')} тренировок/неделю

ТЕКУЩЕЕ СОСТОЯНИЕ:
• Усталость: {fatigue}/10 — риск {risk}
• Качество сна: {sleep}/10 ({sleep_label})
• Боли/дискомфорт: {pain if pain and pain.strip() else 'нет жалоб'}

СТРУКТУРА ОТВЕТА:

## ОЦЕНКА СОСТОЯНИЯ
Общая картина в 2–3 предложениях.

## ГОТОВНОСТЬ К ТРЕНИРОВКЕ
✅ Можно / ⚠️ С ограничениями / 🛑 Противопоказано — и обоснование.

## РЕКОМЕНДАЦИИ НА 24 ЧАСА
• Активность: ...
• Питание и гидратация: ...
• Сон и режим: ...

## РЕКОМЕНДАЦИИ НА 48 ЧАСОВ
Когда вернуться к полноценным тренировкам?

## ПРАКТИЧЕСКИЕ УПРАЖНЕНИЯ ДЛЯ ВОССТАНОВЛЕНИЯ
Конкретный список (растяжка, массаж, ванны и т.д.) с описанием.

## ⚠️ КРАСНЫЕ ФЛАГИ
При каких симптомах — к врачу?

Отвечай на русском языке."""


