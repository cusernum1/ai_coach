from __future__ import annotations

from typing import Optional


def _coach_block(base_program: Optional[str]) -> str:
    """
    Блок, который встраивает базовую программу тренера в промпт.
    Это позволяет тренеру «учить» агента своей методике через чат.
    """
    if not base_program:
        return ""
    return (
        "\n\nБАЗОВАЯ ПРОГРАММА ТРЕНЕРА (учитывай как основу):\n"
        f"{base_program.strip()}\n"
    )


# ══════════════════════════════════════════════════════════════
# План тренировок
# ══════════════════════════════════════════════════════════════
def get_training_plan_prompt(
    athlete: dict,
    weeks: int = 1,
    focus: str = "общая подготовка",
    base_program: Optional[str] = None,
) -> str:
    """Промпт для генерации тренировочного плана."""
    weeks_word = (
        "1 неделю" if weeks == 1
        else f"{weeks} недели" if weeks < 5
        else f"{weeks} недель"
    )
    return f"""Составь детальный план тренировок на {weeks_word}.

ПРОФИЛЬ СПОРТСМЕНА:
• Имя: {athlete['name']}
• Возраст: {athlete.get('age', '—')} лет
• Вид спорта: {athlete.get('sport', '—')}
• Уровень: {athlete.get('level', '—')}
• Цель: {athlete.get('goal', '—')}
• Тренировок в неделю: {athlete.get('sessions_per_week', '—')}
• Акцент: {focus}
{_coach_block(base_program)}
ТРЕБОВАНИЯ К ПЛАНУ (строго соблюдай):
1. Распиши КАЖДЫЙ из 7 дней недели (тренировочные + дни отдыха)
2. Для каждой тренировки укажи:
   — Разминка: 5–10 мин (конкретные упражнения)
   — Основная часть: упражнение → подходы × повторения / время / дистанция
   — Заминка: 5 мин (растяжка)
3. Нагрузка должна соответствовать уровню «{athlete.get('level', '—')}»
4. Соблюдай принцип периодизации (чередуй тяжёлые/лёгкие дни)
5. {'Для нескольких недель добавь прогрессию нагрузки (выделяй каждую неделю заголовком).' if weeks > 1 else ''}
6. В конце — 2–3 рекомендации по питанию и сну

ФОРМАТ ОТВЕТА: Markdown с заголовками H2/H3. Будь конкретным (числа, упражнения, время).
Отвечай на русском языке."""


