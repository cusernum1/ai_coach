# ============================================================
# tools.py — Промпт-шаблоны для инструментов агента
# ============================================================
# Каждая функция формирует детальный промпт для LLM
# на основе профиля спортсмена и входных параметров.
# Шаблоны следуют принципу Chain-of-Thought — структурируют
# вывод, задают формат и перечисляют обязательные секции.
# ============================================================


def get_training_plan_prompt(
    athlete: dict,
    weeks: int = 1,
    focus: str = "общая подготовка",
) -> str:
    """Промпт для генерации тренировочного плана"""
    weeks_word = (
        "1 неделю" if weeks == 1
        else f"{weeks} недели" if weeks < 5
        else f"{weeks} недель"
    )
    return f"""Составь детальный план тренировок на {weeks_word}.

ПРОФИЛЬ СПОРТСМЕНА:
• Имя: {athlete['name']}
• Возраст: {athlete['age']} лет
• Вид спорта: {athlete['sport']}
• Уровень: {athlete['level']}
• Цель: {athlete['goal']}
• Тренировок в неделю: {athlete['sessions_per_week']}
• Акцент: {focus}

ТРЕБОВАНИЯ К ПЛАНУ (строго соблюдай):
1. Распиши КАЖДЫЙ из 7 дней недели (тренировочные + дни отдыха)
2. Для каждой тренировки укажи:
   — Разминка: 5–10 мин (конкретные упражнения)
   — Основная часть: упражнение → подходы × повторения / время / дистанция
   — Заминка: 5 мин (растяжка)
3. Нагрузка должна соответствовать уровню «{athlete['level']}»
4. Соблюдай принцип периодизации (чередуй тяжёлые/лёгкие дни)
5. {'Для нескольких недель добавь прогрессию нагрузки (выделяй каждую неделю заголовком).' if weeks > 1 else ''}
6. В конце — 2–3 рекомендации по питанию и сну

ФОРМАТ ОТВЕТА:
# ПЛАН ТРЕНИРОВОК — {athlete['name']} ({weeks_word})

## НЕДЕЛЯ 1 {'(если недель > 1)' if weeks > 1 else ''}

### День 1 (Понедельник) — [название тренировки]
...

### День 7 (Воскресенье) — Полный отдых
...

## РЕКОМЕНДАЦИИ:
...

Отвечай на русском языке. Будь конкретным (числа, упражнения, время)."""


def get_analysis_prompt(athlete: dict, results: str) -> str:
    """Промпт для анализа результатов тренировок"""
    return f"""Проведи профессиональный анализ результатов тренировок спортсмена.

ПРОФИЛЬ СПОРТСМЕНА:
• {athlete['name']}, {athlete['age']} лет
• Спорт: {athlete['sport']} | Уровень: {athlete['level']}
• Цель: {athlete['goal']} | Нагрузка: {athlete['sessions_per_week']} трен./нед.

ПРЕДОСТАВЛЕННЫЕ РЕЗУЛЬТАТЫ:
{results}

СТРУКТУРА АНАЛИЗА (все пункты обязательны):

## 1. ОЦЕНКА ПРОГРЕССА
Есть ли динамика? Насколько соответствует цели «{athlete['goal']}»?

## 2. СИЛЬНЫЕ СТОРОНЫ
Что идёт хорошо? (минимум 2 пункта)

## 3. СЛАБЫЕ МЕСТА
На что обратить внимание? (конкретно, с обоснованием)

## 4. РЕКОМЕНДАЦИИ
Что изменить в тренировках? (минимум 3 конкретных пункта с деталями)

## 5. РИСК ПЕРЕТРЕНИРОВАННОСТИ
Оценка: X/10. Обоснование. Признаки, на которые смотреть.

## 6. СЛЕДУЮЩИЙ ШАГ
Конкретное действие на ближайшую неделю.

Используй числа и факты из предоставленных данных. Отвечай на русском языке."""


def get_recovery_prompt(
    athlete: dict,
    fatigue: int,
    sleep: int,
    pain: str,
) -> str:
    """Промпт для оценки восстановления"""
    risk = "🔴 ВЫСОКИЙ" if fatigue >= 8 else "🟡 СРЕДНИЙ" if fatigue >= 5 else "🟢 НИЗКИЙ"
    sleep_label = "отличное" if sleep >= 8 else "хорошее" if sleep >= 6 else "плохое"

    return f"""Дай экспертные рекомендации по восстановлению спортсмена.

ПРОФИЛЬ:
• {athlete['name']}, {athlete['sport']}, {athlete['level']}
• Нагрузка: {athlete['sessions_per_week']} тренировок/неделю

ТЕКУЩЕЕ СОСТОЯНИЕ:
• Усталость: {fatigue}/10 — риск {risk}
• Качество сна: {sleep}/10 ({sleep_label})
• Боли/дискомфорт: {pain if pain and pain.strip() else 'нет жалоб'}

СТРУКТУРА ОТВЕТА:

## ОЦЕНКА СОСТОЯНИЯ
Общая картина в 2–3 предложениях.

## ГОТОВНОСТЬ К ТРЕНИРОВКЕ
✅ Можно / ⚠️ С ограничениями / 🛑 Противопоказано — и обоснование.

## РЕКОМЕНДАЦИИ НА 24 ЧАСА
• Активность: ...
• Питание и гидратация: ...
• Сон и режим: ...

## РЕКОМЕНДАЦИИ НА 48 ЧАСОВ
Когда вернуться к полноценным тренировкам?

## ПРАКТИЧЕСКИЕ УПРАЖНЕНИЯ ДЛЯ ВОССТАНОВЛЕНИЯ
Конкретный список (растяжка, массаж, ванны и т.д.) с описанием.

## ⚠️ КРАСНЫЕ ФЛАГИ
При каких симптомах — к врачу?

Отвечай на русском языке."""


def get_nutrition_prompt(
    athlete: dict,
    training_day: str = "тренировочный",
    specific_goal: str = None,
) -> str:
    """Промпт для рекомендаций по питанию"""
    goal = specific_goal or athlete["goal"]

    return f"""Разработай подробные рекомендации по питанию для спортсмена.

ПРОФИЛЬ:
• {athlete['name']}, {athlete['age']} лет
• Спорт: {athlete['sport']} | Уровень: {athlete['level']}
• Цель: {goal}
• Тренировок/нед.: {athlete['sessions_per_week']}
• Тип дня: {training_day}

СТРУКТУРА РЕКОМЕНДАЦИЙ:

## 1. КБЖУ (ориентировочно)
| Показатель | Значение |
|------------|----------|
| Калории    | ... ккал |
| Белки      | ... г (... г/кг) |
| Углеводы   | ... г |
| Жиры       | ... г |

## 2. РЕЖИМ ПИТАНИЯ
• До тренировки (за сколько часов, что есть): ...
• {'Во время тренировки (если >60 мин): ...' if training_day == 'тренировочный' else ''}
• После тренировки (белково-углеводное окно): ...

## 3. ПРИМЕРНОЕ МЕНЮ — {training_day.upper()} ДЕНЬ
**Завтрак:** ...
**Перекус 1:** ...
**Обед:** ...
**Перекус 2:** ...
**Ужин:** ...

## 4. ГИДРАТАЦИЯ
• Суточная норма воды: ... л
• До/во время/после тренировки: ...

## 5. ДОБАВКИ
Что реально нужно для цели «{goal}»? Что НЕ нужно?

## 6. ПРОДУКТЫ, МЕШАЮЩИЕ ПРОГРЕССУ
Список продуктов, которые замедляют достижение цели, с объяснением почему.

Давай конкретные цифры, порции, названия продуктов.
Отвечай на русском языке."""


def get_workload_analysis_prompt(athlete: dict, logs_summary: str) -> str:
    """
    НОВЫЙ промпт: анализ тренировочной нагрузки по журналу.
    Использует данные RPE и статусы выполнения.
    """
    return f"""Проведи анализ тренировочной нагрузки на основе журнала тренировок.

ПРОФИЛЬ:
• {athlete['name']}, {athlete['age']} лет
• Спорт: {athlete['sport']} | Уровень: {athlete['level']}
• Цель: {athlete['goal']}

ЖУРНАЛ ТРЕНИРОВОК:
{logs_summary}

СТРУКТУРА АНАЛИЗА:

## 1. ОБЩАЯ НАГРУЗКА
Оцени суммарную нагрузку за период. Средний RPE. Динамика нагрузки.

## 2. ВЫПОЛНЕНИЕ ПЛАНА
Процент выполненных тренировок. Паттерны пропусков (если есть).

## 3. СООТНОШЕНИЕ ОСТРОЙ/ХРОНИЧЕСКОЙ НАГРУЗКИ
На основе RPE — есть ли резкие скачки? Плавная ли прогрессия?

## 4. РИСКИ
Признаки перетренированности, монотонности или недостаточной нагрузки.

## 5. КОРРЕКТИРОВКИ
Конкретные рекомендации по изменению плана.

Будь конкретным, опирайся на данные. Отвечай на русском языке."""
